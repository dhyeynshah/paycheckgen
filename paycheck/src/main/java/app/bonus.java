    private void fetchBonusData() {
        try {
                String eDB = "jdbc:mysql://localhost:3306/employee_id";
                String eUser = "root";
                String ePassword = "";
                String sql = "SELECT * FROM bonus";
                Connection con = DriverManager.getConnection(eDB, eUser, ePassword);
                PreparedStatement stmt = con.prepareStatement(sql);
                ResultSet rs = stmt.executeQuery();

                // Create a table model to hold the data
                DefaultTableModel model = new DefaultTableModel();
                // Add columns to the table model
                model.addColumn("Bonus ID");
                model.addColumn("Employee ID");
                model.addColumn("Amount");
                model.addColumn("Reason");

                // Populate the table model with data from the result set
                while (rs.next()) {
                    model.addRow(new Object[]{
                            rs.getInt("bonus_id"),
                            rs.getInt("emp_id"),
                            rs.getDouble("bonus_val"),
                            rs.getString("bonus_reason")
                    });
                }
                
                
                // Set the table model to the JTable
                jTable1.setModel(model);
                jTable2.setModel(model);

                // Close resources
                rs.close();
                stmt.close();
                con.close();
            } catch (SQLException e) {
                e.printStackTrace();
            }
    }

