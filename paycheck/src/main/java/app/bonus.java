    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        double amount = Double.parseDouble(this.amount.getText());
        String description = reason.getText();

        // Get the employee ID from the JTextField
        String empId = empid.getText();

        int bonusId;
        boolean isUnique = false;

        // Keep generating new reimbursement IDs until a unique one is found
        while (!isUnique) {
            bonusId = (int)(Math.random() * 999999);
            isUnique = isReimbursementIdUnique(bonusId);

            if (isUnique) {
                try {
                    // Insert the data into the reimbursement table
                    String sql = "INSERT INTO bonus (bonus_id, emp_id, bonus_val, bonus_reason) VALUES (?, ?, ?, ?)";
                    Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/employee_id", "root", "");
                    PreparedStatement stmt = con.prepareStatement(sql);
                    stmt.setInt(1, bonusId);
                    stmt.setString(2, empId);
                    stmt.setDouble(3, amount);
                    stmt.setString(4, description);

                    // Execute the insert statement
                    int rowsAffected = stmt.executeUpdate();

                    if (rowsAffected > 0) {
                        JOptionPane.showMessageDialog(null, "Bonus added successfully");
                    } else {
                        JOptionPane.showMessageDialog(null, "Failed to add bonus");
                    }

                    // Close resources
                    stmt.close();
                    con.close();
                } catch (SQLException e) {
                    e.printStackTrace();
                    JOptionPane.showMessageDialog(null, "Error: " + e.getMessage());
                }
            }
        }
        }

        private boolean isReimbursementIdUnique(int reimbursementId) {
            try {
                Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/employee_id", "root", "");
                String checkSql = "SELECT COUNT(*) FROM bonus WHERE bonus_id = ?";
                PreparedStatement checkStmt = con.prepareStatement(checkSql);
                checkStmt.setInt(1, reimbursementId);
                ResultSet rs = checkStmt.executeQuery();
                rs.next();
                int count = rs.getInt(1);

                // Close resources
                rs.close();
                checkStmt.close();
                con.close();

                return count == 0;
            } catch (SQLException e) {
                e.printStackTrace();
                JOptionPane.showMessageDialog(null, "Error: " + e.getMessage());
                return false;
            }

    }//GEN-LAST:event_jButton5ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
            int bIdToDelete = Integer.parseInt(jTextField1.getText());
            try {
            // Establish connection to the database
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/employee_id", "root", "");

            // Prepare the SQL statement for deletion
            String sql = "DELETE FROM bonus WHERE bonus_id = ?";
            PreparedStatement stmt = con.prepareStatement(sql);
            stmt.setInt(1, bIdToDelete);

            // Execute the deletion statement
            int rowsAffected = stmt.executeUpdate();

            // Check if any rows were affected by the deletion
            if (rowsAffected > 0) {
                JOptionPane.showMessageDialog(null, "Bonus with bonus_id " + bIdToDelete + " deleted successfully");
            } else {
                JOptionPane.showMessageDialog(null, "No bonus found with bonus_id " + bIdToDelete);
            }

            // Close resources
            stmt.close();
            con.close();
        } catch (SQLException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, "Error: " + e.getMessage());
        }
    }//GEN-LAST:event_jButton2ActionPerformed

