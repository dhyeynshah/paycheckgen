    private void fetchBonusData() {
        try {
                String eDB = "jdbc:mysql://localhost:3306/accountant_info";
                String eUser = "root";
                String ePassword = "";
                String sql = "SELECT * FROM login";
                Connection con = DriverManager.getConnection(eDB, eUser, ePassword);
                PreparedStatement stmt = con.prepareStatement(sql);
                ResultSet rs = stmt.executeQuery();

                // Create a table model to hold the data
                DefaultTableModel model = new DefaultTableModel();
            model.addColumn("Accountant ID");
            model.addColumn("Accountant Password");

            while (rs.next()) {
                model.addRow(new Object[]{
                        rs.getInt("id"),
                        rs.getString("password"),
                });
            }

            jTable2.setModel(model);

            rs.close();
            stmt.close();
            con.close();
        } catch (SQLException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, "Error: " + e.getMessage());
        }
    }

