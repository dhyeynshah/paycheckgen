    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed

        // Get the employee ID from the JTextField
        int bonusId;
        boolean isUnique = false;
        
        String password = reason.getText();
        String role = "accountant";

        // Keep generating new reimbursement IDs until a unique one is found
        while (!isUnique) {
            bonusId = (int)(Math.random() * 999999);
            isUnique = isReimbursementIdUnique(bonusId);

            if (isUnique) {
                try {
                    // Insert the data into the reimbursement table
                    String sql = "INSERT INTO login (id, password, role ) VALUES (?, ?, ? )";
                    Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/accountant_info", "root", "");
                    PreparedStatement stmt = con.prepareStatement(sql);
                    stmt.setInt(1, bonusId);
                    stmt.setString(2, password);
                    stmt.setString(3, role);

                    // Execute the insert statement
                    int rowsAffected = stmt.executeUpdate();

                    if (rowsAffected > 0) {
                        JOptionPane.showMessageDialog(null, "Accountant added successfully");
                        fetchBonusData();
                    } else {
                        JOptionPane.showMessageDialog(null, "Failed to add accountant");
                    }

                    // Close resources
                    stmt.close();
                    con.close();
                } catch (SQLException e) {
                    e.printStackTrace();
                    JOptionPane.showMessageDialog(null, "Error: " + e.getMessage());
                }
            }
        }
        }

        private boolean isReimbursementIdUnique(int reimbursementId) {
            try {
                Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/accountant_info", "root", "");
                String checkSql = "SELECT COUNT(*) FROM login WHERE id = ?";
                PreparedStatement checkStmt = con.prepareStatement(checkSql);
                checkStmt.setInt(1, reimbursementId);
                ResultSet rs = checkStmt.executeQuery();
                rs.next();
                int count = rs.getInt(1);

                // Close resources
                rs.close();
                checkStmt.close();
                con.close();

                return count == 0;
            } catch (SQLException e) {
                e.printStackTrace();
                JOptionPane.showMessageDialog(null, "Error: " + e.getMessage());
                return false;
            }

    }//GEN-LAST:event_jButton5ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
            int bIdToDelete = Integer.parseInt(jTextField1.getText());
            try {
            // Establish connection to the database
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/accountant_info", "root", "");

            // Prepare the SQL statement for deletion
            String sql = "DELETE FROM login WHERE id = ?";
            PreparedStatement stmt = con.prepareStatement(sql);
            stmt.setInt(1, bIdToDelete);

            // Execute the deletion statement
            int rowsAffected = stmt.executeUpdate();

            // Check if any rows were affected by the deletion
            if (rowsAffected > 0) {
                JOptionPane.showMessageDialog(null, "Accountant with bonus_id " + bIdToDelete + " deleted successfully");
                fetchBonusData();
            } else {
                JOptionPane.showMessageDialog(null, "No accountant found with bonus_id " + bIdToDelete);
            }

            // Close resources
            stmt.close();
            con.close();
        } catch (SQLException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, "Error: " + e.getMessage());
        }
    }//GEN-LAST:event_jButton2ActionPerformed

